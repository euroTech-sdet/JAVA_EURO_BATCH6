package days.day3;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Spain\nItalyEnglandNorway");
        // \n    insert new line into the text
        System.out.println("Spain\tItaly\tEngland\tNorway\tsweden");
        //  \t put a tab into the text after this point
        System.out.println("Spain\bItalyEnglandNorway");
        // \b backspace

        System.out.println("   Ali said taha : \"Come here\" ");
        System.out.println("   Ali said taha : \\Come here\\ ");
        System.out.println("   Ali said taha : 'Come here' ");
        System.out.println("   Ali said taha : \"Come here\" ");
        System.out.println("hello it is almost over");

        System.out.println("   Ali said taha : \\\\Come here\\\\ ");


    }
}
